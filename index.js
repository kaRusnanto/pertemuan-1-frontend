// minimalist.push
let fruits = ["apple", "banana"];
fruits.push("cherry", "watermelon"); 
console.log(fruits); 
document.write(fruits)

// minimalist.shift
let bers = ["apple", "banana", "cherry"];
bers.shift();
console.log(bers);
document.write(bers);

// minimalist.unshift
let buah = ["banana", "cherry"];
buah.unshift("apple", "date");
console.log(buah);
document.write(buah);


